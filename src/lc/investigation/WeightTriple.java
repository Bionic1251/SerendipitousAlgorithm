package lc.investigation;

public class WeightTriple {
	private double wr;
	private double wd;
	private double wu;

	public WeightTriple(double wr, double wd, double wu) {
		this.wr = wr;
		this.wd = wd;
		this.wu = wu;
	}

	public double getWr() {
		return wr;
	}

	public double getWd() {
		return wd;
	}

	public double getWu() {
		return wu;
	}
}
